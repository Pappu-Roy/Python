# Checking different data types in Python
x = "Hello, World!"  # string
y = 20               # integer
z = 20.5             # float

print(x)
print(type(x))
print(y)
print(type(y))
print(z)
print(type(z))

x = 1j              # complex
print(x)
print(type(x))

x = ["apple", "banana", "cherry"]

#display x:
print(x)

#display the data type of x:
print(type(x)) 

x = ("apple", "banana", "cherry") # tuple
print(x)
print(type(x))

x = {"name" : "John", "age" : 36} # dictionary

#display x:
print(x)

#display the data type of x:
print(type(x)) 

# set
x = {"apple", "banana", "cherry", "apple"} 

#display x:
print(x)

#display the data type of x:
print(type(x)) 

